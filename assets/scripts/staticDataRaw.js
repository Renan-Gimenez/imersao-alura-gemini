const resultsPerPage = 20;

let pagination; // Declara a variável de paginação no escopo global
let currentPage = 1; // Página inicial

// Função de paginação
function paginate(array, resultsPerPage, searchValue = "") {
  const filteredArray = searchValue
    ? array.filter((item) =>
        item.name.toLowerCase().includes(searchValue.toLowerCase())
      )
    : array;

  const totalPages = Math.ceil(filteredArray.length / resultsPerPage);

  return function getPage(pageNumber) {
    if (pageNumber < 1 || pageNumber > totalPages) {
      return [];
    }

    const start = (pageNumber - 1) * resultsPerPage;
    const end = start + resultsPerPage;

    return filteredArray.slice(start, end);
  };
}

// Função para renderizar a página
function renderPage(pageNumber) {
  const paginationControl = document.querySelector(".pagination-control");
  paginationControl.style.display = "flex";

  if (typeof pagination !== "function") {
    console.error("pagination is not a function");
    return;
  }

  const list = document.querySelector(".characters-list");
  list.innerHTML = "";

  const currentPageData = pagination(pageNumber);
  if (currentPageData.length === 0) {
    paginationControl.style.display = "none";

    list.innerHTML =
      '<div class="character-not-found"><span style="color: var(--color-text-primary); font-size: 20px">Character not found!</span></div>';
  } else {
    currentPageData.forEach((character) => {
      const div = document.createElement("div");
      div.className = "character-div";
      div.innerHTML = `
                <div class="character-info">
                    <h2 class="character-name">${character.name}</h2>
                    <p class="character-label">
                        Species: <span class="character-value">${character.species}</span>
                    </p>
                    <p class="character-label">
                        Status: <span class="character-value">${character.status}</span>
                    </p>
                    <div class="separator"></div>
                    <p class="character-label">
                        Origin: <br /><span class="character-value">${character.origin}</span>
                    </p>
                    <p class="character-label">
                        Last known location: <br />
                        <span class="character-value">${character.location}</span>
                    </p>
                </div>
                <img src="${character.image}" class="character-image" />
            `;
      list.appendChild(div);
    });
  }

  const searchValue = document.querySelector(".search-bar-input").value.trim();
  const filteredArrayLength = searchValue
    ? staticRawCharacters.filter((item) =>
        item.name.toLowerCase().includes(searchValue.toLowerCase())
      ).length
    : staticRawCharacters.length;

  updatePaginationControls(pageNumber, filteredArrayLength);
}

